    private void txtDataDiaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDataDiaKeyPressed

        try {
            if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
                //botao buscar vendas por per√≠odo
                //receber as datas
                DateTimeFormatter formato = DateTimeFormatter.ofPattern("dd/MM/yyyy");
                LocalDate data = LocalDate.parse(txtDataDia.getText(), formato);

                double total_venda;
                VendasDAO dao = new VendasDAO();
                total_venda = dao.listarTotalVendas(data);

                txtTotalDia.setText(String.valueOf(total_venda));
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, "Data Incorreta. " + erro);
        }
    }//GEN-LAST:event_txtDataDiaKeyPressed

